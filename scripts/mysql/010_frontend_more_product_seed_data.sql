INSERT INTO ims_inventory (sku_id, available_stock, locked_stock, version, deleted)
SELECT s.id,
       CASE
           WHEN s.price >= 500 THEN 30
           WHEN s.price >= 100 THEN 80
           ELSE 150
       END AS available_stock,
       0 AS locked_stock,
       0 AS version,
       0 AS deleted
FROM pms_sku s
WHERE s.sku_code LIKE 'FRONT-S-%'
ON DUPLICATE KEY UPDATE available_stock = VALUES(available_stock),
                        locked_stock = VALUES(locked_stock),
                        deleted = VALUES(deleted);
